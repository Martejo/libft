#include "../libft.h"

void	ft_putchar2_endl(char **char2)
{
	size_t	i;

	i = 0;
	while (char2[i])
	{
		ft_putstr_fd(char2[i], 1);
		write(1, "\n", 1);
		i++;
	}
}
